import java.util.*;

public class type_casting_character2ASCII {
    public static void main(String args[]){
            char ch = 'a';
            //char ch=str.charAt(0); 
            System.out.println("The entered character is=  ");
            System.out.println(ch);
            int a = ch;
            System.out.println("And the ASCII value of the same is= ");
            System.out.println(a);
           
        }
}

// We are converting it to it's ASCII code