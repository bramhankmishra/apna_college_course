public class unary_opr{
    public static void main(String args[]){
        int a = 10;  // pre-increment
        int b = ++a;
        System.out.println(a);
        System.out.println(b);
    
        int x = 10;   // post-increment 
        int y = x++;
        System.out.println(x);
        System.out.println(y);
    }
}