public class app_of_arith_operators{
    public static void main(String args[]){
        int a= 10;
        int b= 20;
        System.out.println("Add = " + (b + a));
        System.out.println("Sub = " + (b - a));
        System.out.println("Mul = " + (b * a));
        System.out.println("Div = " + (b / a));
        System.out.println("Remainder/modulo = " + (b % a));            
    }
}